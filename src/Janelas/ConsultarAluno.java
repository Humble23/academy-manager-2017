    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        CadastroAluno ca = new CadastroAluno();
        if(jTable1.isRowSelected(linha)){
        ca.setVisible(true);
        ca.jButton1.setVisible(false);
        Aluno aluno = new Aluno();
        aluno.abrirConexao();
        ca.jRadioButton1.setEnabled(false);
        ca.jRadioButton2.setEnabled(false);
        ca.jButton3.setVisible(false);
        ca.jTabbedPane1.setEnabled(true);
        if(aluno.consultar2(matricula).getSexo().equals("M")){
                ca.jRadioButton1.setSelected(true);
            }else{
                ca.jRadioButton2.setSelected(true);
            }
        ca.jTextField1.setText(""+aluno.consultar2(matricula).getMatricula());
        ca.jTextField3.setText(""+aluno.consultar2(matricula).getData_matricula());
        ca.jTextField2.setText(""+aluno.consultar2(matricula).getNome());
        ca.jTextField7.setText(""+aluno.consultar2(matricula).getNome());
        ca.jTextField4.setText(""+aluno.consultar2(matricula).getData_nasc());
        ca.jTextField6.setText(""+aluno.consultar2(matricula).getEndereco());
        ca.jTextField11.setText(""+aluno.consultar2(matricula).getTelefone());
        ca.jTextField13.setText(""+aluno.consultar2(matricula).getEmail());
        aluno.fecharConexao();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o aluno que deseja editar seus dados");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

