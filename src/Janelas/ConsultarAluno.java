    public void consultar(){
        modelo = (DefaultTableModel)jTable1.getModel();
        String pesquisa = jTextField1.getText();
        String filtro = jComboBox1.getSelectedItem().toString().toLowerCase();
        modelo.setNumRows(0);
        Aluno aluno = new Aluno();
        aluno.abrirConexao();
        for (Aluno a : aluno.consultar(filtro, pesquisa)){
            modelo.addRow(new Object[]{a.getMatricula(),a.getNome(),a.getSexo(),a.getData_nasc(),a.getData_matricula(),a.getEndereco(),a.getTelefone(),a.getEmail()});
        }
        aluno.fecharConexao();
    }
